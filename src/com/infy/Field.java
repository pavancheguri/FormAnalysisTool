package com.infy;

public class Field {

	private String ref;
	private String tagName;
	private String length;
	private String character;
	private String manditory;
	private String delAfterUse;
	private String endLine;
	private String generate;
	private String startPostion;
	public String getRef() {
		return ref;
	}
	public void setRef(String ref) {
		this.ref = ref;
	}
	public String getTagName() {
		return tagName;
	}
	public void setTagName(String tagName) {
		this.tagName = tagName;
	}
	public String getLength() {
		return length;
	}
	public void setLength(String length) {
		this.length = length;
	}
	public String getCharacter() {
		return character;
	}
	public void setCharacter(String character) {
		this.character = character;
	}
	public String getManditory() {
		return manditory;
	}
	public void setManditory(String manditory) {
		this.manditory = manditory;
	}
	public String getDelAfterUse() {
		return delAfterUse;
	}
	public void setDelAfterUse(String delAfterUse) {
		this.delAfterUse = delAfterUse;
	}
	public String getEndLine() {
		return endLine;
	}
	public void setEndLine(String endLine) {
		this.endLine = endLine;
	}
	public String getGenerate() {
		return generate;
	}
	public void setGenerate(String generate) {
		this.generate = generate;
	}
	public String getStartPostion() {
		return startPostion;
	}
	public void setStartPostion(String startPostion) {
		this.startPostion = startPostion;
	}
}


